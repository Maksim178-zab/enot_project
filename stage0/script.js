// Функция скачивания шаблона
document.getElementById('downloadTemplateBtn').addEventListener('click', () => {
    // Создаём содержимое шаблона
    const content = `Отчёт по этапу 0: Очистка данных

Команда: ____________________
Тема проекта: ____________________

Исходные данные:
- Количество записей до очистки: _____
- Источник данных: ____________________

Выполненные действия:
1. Удалено дубликатов: _____
2. Исправлено ошибок: _____
3. Заполнено пропусков: _____

Результат:
- Количество записей после очистки: _____

Приложения:
- Исходная таблица (приложена)
- Очищенная таблица (приложена)

Дата: ____________________
`;

    // Создаём Blob и скачиваем
    const blob = new Blob([content], { type: 'application/msword' });
    const link = document.createElement('a');
    link.href = URL.createObjectURL(blob);
    link.download = 'Отчёт_этап0_очистка_данных.doc';
    link.click();
    URL.revokeObjectURL(link.href);
});